package model;

public class Hoten {
	private String hodem;
	private String ten;

	// Constructor
	public Hoten() {
	}

	// Getters and Setters
	public String getHodem() {
		return hodem;
	}

	public void setHodem(String hodem) {
		this.hodem = hodem;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}
}
